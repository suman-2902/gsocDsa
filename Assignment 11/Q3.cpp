class Solution {
public:
    int peakIndexInMountainArray(vector<int>& ar) {
        int n=ar.size(),i;
        
        if(n>=3){
            
            for( i=0;i<n-1;i++)
                
            {
                return
        distance(ar.begin(),max_element(ar.begin(),ar.end()));
            }
        }   
        
         return 0;
        
    }
};
